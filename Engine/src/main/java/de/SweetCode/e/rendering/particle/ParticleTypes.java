package de.SweetCode.e.rendering.particle;

/**
 * All currently implemented particle types.
 */
public enum ParticleTypes {

    /**
     * Will draw circular particles.
     */
    CIRCLE,

    /**
     * Will draw squared particles.
     */
    SQUARE,

    /**
     * Will choose on of the other normal types.
     */
    RANDOM;

}
